# Espressione condizionale (operatore ternario)
# È una scorciatoia dell'if-else
# Assegna o stampa un valore in base a una condizione
# Valore_se_vero if condizione else valore_se_falso

num = 5

# Controllo se il numero è positivo o negativo
print("Positive" if num > 0 else "Negative")

num = 2

# Controllo se il numero è pari o dispari
print("Even" if num % 2 == 0 else "Odd")
