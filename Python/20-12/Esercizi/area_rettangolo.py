# Area = lunghezza * larghezza

print("Benvenuto nel programma che calcola l'area di un rettangolo.")
lunghezza = float(input("Inserire la lunghezza: "))
larghezza = float(input("Inserire la larghezza: "))

area = lunghezza * larghezza

print(f"L'area è {area} cm²") # Per mettere potenza si fa numlock + alt + 0178