import math

# Costanti matematiche
print(math.pi)  # Pigreco
print(math.e)   # Costante esponenziale

x = 9

# Radice quadrata
result = math.sqrt(x)
print(result)

# Approssimazione per eccesso
result = math.ceil(x)
print(result)

# Approssimazione per difetto
result = math.floor(x)
print(result)

# Potenza
result = math.pow(x, 2)
print(result)

# Valore assoluto
result = math.fabs(-x)
print(result)

# Logaritmo naturale (base e)
result = math.log(x)
print(result)

# Logaritmo in base 10
result = math.log10(x)
print(result)
